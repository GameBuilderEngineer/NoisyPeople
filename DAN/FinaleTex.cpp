#include "Finaletex.h"

//===========================================================
// using宣言
//===========================================================
using namespace finaleTexNS;

//===========================================================
//コンストラクタ
//===========================================================
FinaleTex::FinaleTex()
{
	fin = new Sprite;
}

//===========================================================
//デストラクタ
//===========================================================
FinaleTex::~FinaleTex()
{
	delete fin;
}

//===========================================================
//初期化
//===========================================================
void FinaleTex::initialize()
{
	BasicUI::assingSize(WIDTH_FIN, HEIGHT_FIN);
	BasicUI::assingPosition(POSITION_FIN);
	BasicUI::assingColor(COLOR_FIN);
	BasicUI::initialize(fin, textureNS::reference(textureNS::FIN));

	alpha = 0;
}

//===========================================================
//描画
//===========================================================
void FinaleTex::render()
{
	fin->render();
}

//============================================================
//更新
//============================================================
void FinaleTex::update()
{
	if (alpha < 255)
	{
		alpha += ALPHA_SPEED;
		fin->setAlphaAllVertex(alpha);
		fin->render();
	}
	else
	{
		fin->setAlphaAllVertex(255);
		fin->render();
	}
}