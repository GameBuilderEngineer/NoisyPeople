#include "ModeFlag.h"

//===================================================================================================================================
//【グローバル変数】
//===================================================================================================================================
//設定切替フラグ
bool modePresentation = false;

//===================================================================================================================================
//【取得関数】
//===================================================================================================================================
bool getModeFlag(int type)
{
	switch (type)
	{
	case MODE_PRESENTATION:	return modePresentation; break;
	}
}
//===================================================================================================================================
//【セット関数】
//===================================================================================================================================
void setModeFlag(int type,bool flag)
{
	switch (type)
	{
	case MODE_PRESENTATION:	modePresentation = flag; break;
	}
}