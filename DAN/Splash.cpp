Splash::Splash()
{
	// 現在のシーン(スプラッシュ)
	sceneName = "Scene -Splash-";
	// 次のシーン(タイトル)
	nextScene = SceneList::TITLE;

	//サウンドの再生
	SEManager::SwitchAudioBuffer(SceneList::SPLASH);	//シーンの更新
	SoundInterface::playSound(ENDPOINT_VOICE_LIST::ENDPOINT_SE, SPLASH_SE_LIST::SPLASH_SE_01, false);
}

