#include "SplashSprite.h"
#include "TextureLoader.h"

//===================================================================================================================================
//【using宣言】
//===================================================================================================================================
using namespace splashSpriteNS;

//===================================================================================================================================
//【コンストラクタ】
//===================================================================================================================================
SplashSprite::SplashSprite()
{
	initialize(
		*textureNS::reference(textureNS::SPLASH),	//テクスチャポインタ
		SpriteNS::CENTER,							//原点
		WIDTH,										//幅
		HEIGHT,										//高さ
		POSITION,									//位置
		ROTATION,									//回転
		COLOR										//色
	);
}

//===================================================================================================================================
//【デストラクタ】
//===================================================================================================================================
SplashSprite::~SplashSprite()
{
}

//===================================================================================================================================
//【更新】
//===================================================================================================================================
void SplashSprite::update(float rate)
{
	float sinValue = max(sinf(D3DX_PI * rate), 0);
	setAlphaAllVertex((int)((float)255 * sinValue));
}
