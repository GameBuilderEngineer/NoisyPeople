#include "Enemy.h"
using namespace enemyNS;

int Enemy::cntID = 0;
int Enemy::numOfEnemy = 0;


//=============================================================================
// コンストラクタ
//=============================================================================
Enemy::Enemy(): StaticMeshObject(staticMeshNS::getStaticMesh(staticMeshNS::SAMPLE_STATIC_MESH))
{
	numOfEnemy++;
}


//=============================================================================
// デストラクタ
//=============================================================================
Enemy::~Enemy()
{
	numOfEnemy--;
}


//=============================================================================
// 更新処理
//=============================================================================
void Enemy::update()
{

}


//=============================================================================
// 描画処理
//=============================================================================
void Enemy::render()
{
}


//=============================================================================
// エネミーデータを設定
//=============================================================================
void Enemy::setEnemyData(EnemyData enemyData)
{
	id = cntID++;
	type = enemyData.type;
}


//=============================================================================
// Getter
//=============================================================================
int Enemy::getID() { return id; }
int Enemy::getType() { return type; }
int Enemy::getNumOfEnemy() { return numOfEnemy; }



